# using the range() FUNCTION, one can set the frequency of numbers to be
# included in the series. This would be specified as the third argument
# within the parentheses.

even_numbers = list(range(1,11,2))
print(even_numbers)

# in the example above, the third argument '2' within the parenthes specifies
# that the series should include every two numbers within the range - the
# first two arguments.
